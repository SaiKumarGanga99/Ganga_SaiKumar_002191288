    public String updatedTime() {
        DateTimeFormatter d = DateTimeFormatter.ofPattern("dd/MM/yyyy HH:mm:ss");
        LocalDateTime ldt = LocalDateTime.now();
        String date = d.format(ldt);
        txtUpdatedTime.setText(date);
        return date;
    }
