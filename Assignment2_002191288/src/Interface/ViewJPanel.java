    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:

        car.setmanufactureType(txtmanufactureType.getText());
        car.setavailabilityStatus(Boolean.parseBoolean(txtAvailable.getText()));
        car.setCity(txtCity.getText());
        car.setmaintenceCertificateExpiryStatus(Boolean.parseBoolean(txtCertificate.getText()));
        car.setManufacturedYear(Integer.parseInt(txtManufacturedYear.getText()));
        car.setModelNumber(txtModelNumber.getText());
        car.setAvailableSeats(Integer.parseInt(txtMaxSeats.getText()));
        car.setSerialNumber(txtSerialNumber.getText());

        btnSave.setEnabled(false);
        btnUpdate.setEnabled(true);

        btnSave.setEnabled(false);
        btnUpdate.setEnabled(true);
        txtmanufactureType.setEnabled(false);
        txtAvailable.setEnabled(false);
        txtCertificate.setEnabled(false);
        txtCity.setEnabled(false);
        txtModelNumber.setEnabled(false);
        txtMaxSeats.setEnabled(false);
        txtSerialNumber.setEnabled(false);
        txtManufacturedYear.setEnabled(false);
        fleetDirectory.setFleetUpdatedDate(updatedTime());

    }//GEN-LAST:event_btnSaveActionPerformed

